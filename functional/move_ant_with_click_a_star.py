# function that draws the ant to the screen
def drawant(cord): #draws a red rectangle for the ant
    antsize = 25
    ant = pygame.Rect(cord[1], cord[0], antsize, antsize)
    pygame.draw.rect(SCREEN, RED, ant)

def pre_ant():
    antsize = 25
    ant = pygame.Rect(0,0, antsize, antsize)
    pygame.draw.rect(SCREEN, RED, ant)
    current_ant = Ant((0,0), ant)
    return current_ant

# function that converts the astar path to a pixel format path
def gridpath(path):
    grid_path = []
    for cord in path:
        # print('cord in gridpath:')
        # print(cord)
        new_cord = []
        x = 25*cord[0]
        y = 25*cord[1]
        new_cord.append(x)
        new_cord.append(y)
        grid_path.append(new_cord)
    return grid_path

