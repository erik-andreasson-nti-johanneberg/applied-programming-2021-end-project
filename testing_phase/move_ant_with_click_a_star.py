# function that draws the grid layout to the screen
def drawGrid():
    blockSize = 25 #Set the size of the grid block
    for x in range(0, WINDOW_WIDTH, blockSize):
        for y in range(0, WINDOW_HEIGHT, blockSize):
            rect = pygame.Rect(x, y, blockSize, blockSize)
            pygame.draw.rect(SCREEN, WHITE, rect, 1)

# function that draws the ant to the screen
def drawant(cord): #draws a red rectangle for the ant
    antsize = 25
    ant = pygame.Rect(cord[1], cord[0], antsize, antsize)
    pygame.draw.rect(SCREEN, RED, ant)

def pre_ant():
    antsize = 25
    ant = pygame.Rect(0,0, antsize, antsize)
    pygame.draw.rect(SCREEN, RED, ant)
    current_ant = Ant((0,0), ant)
    return current_ant

# function that converts the astar path to a pixel format path
def gridpath(path):
    grid_path = []
    for cord in path:
        new_cord = []
        x = 25*cord[0]
        y = 25*cord[1]
        new_cord.append(x)
        new_cord.append(y)
        grid_path.append(new_cord)
    return grid_path

